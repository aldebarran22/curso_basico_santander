"""
Tipo de las variables.
Operaciones con variables
"""

# Operaciones con variables enteras
numero = 100
print(numero, type(numero))
numero2 = 200
print(numero + numero2)

# Operaciones con variables de texto:
txt1 = "100"
print(txt1, type(txt1))
txt2 = '200'
print(txt1 + txt2)


