"""
Operadores aritméticos en Python
"""

# div. real: la media de dos números
n1 = 12
n2 = 3
media =  (n1 + n2) / 2
print('media: ', media)

# div entera: Cuanto billetes de 50 necesito para sacar 230 eur
importe = 230
numBilletes = importe // 50
print('numBilletes: ', numBilletes)
resto = importe % 50
print('Resto: ', resto)
