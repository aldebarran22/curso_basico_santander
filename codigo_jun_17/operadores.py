# Calcular la media de dos números: div. real
n1 = 34
n2 = 56
media = (n1 + n2) / 2
print('media:', media)

# Div. entera y el módulo: %
importe = 230
billetes50 = importe // 50
resto = importe % 50
print(f"Importe: {importe}, billetes de 50: {billetes50} y el resto: {resto}")

# Potencia:
print(2**10)

