"""
Cadenas en Python:
Secuencias de escape (\n, \t) y cadenas raw.
"""
nombre = "Jorge"
print("hola\nadios")
print("hola\tadios")
print('El nombre es: "', nombre, '"')
print(r"hola\tadios")

# Operadores a nivel de cadena:
print("hola" + nombre)  # Concatenar
print("hola" * 5)  # Concatenar n-veces
